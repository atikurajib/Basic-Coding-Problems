// Demonstrating ASCII values in Java.

public class Main {
    public static void main(String[] args) {
        // Initialize char variables with ASCII values
        char my1 = 75;  // ASCII value 75 corresponds to 'K'
        char my2 = 76;  // ASCII value 76 corresponds to 'L'
        char my3 = 77;  // ASCII value 77 corresponds to 'M'
        
        // Print the characters corresponding to the ASCII values
        System.out.println(my1);  // Output: K
        System.out.println(my2);  // Output: L
        System.out.println(my3);  // Output: M
    }
}
