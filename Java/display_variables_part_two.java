// Demonstrating string concatenation and basic arithmetic in Java.

public class Main {
    public static void main(String[] args) {
        
        // String variables: store first and last names
        String firstName = "Ahmed";
        String lastName = "Robin";
        
        // Concatenating first and last name to form the full name
        String fullName = firstName + " " + lastName;
        // Displaying the full name
        System.out.println(fullName);
        
        // Integer variables: store numbers
        int x = 23;
        int y = 27;
        
        // Uncomment the line below to declare and initialize x and y in a single statement
        // int x = 23, y = 27;
        
        // Displaying the sum of x and y
        System.out.println(x + y);
    }
}
