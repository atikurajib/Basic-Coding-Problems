// Decrement Operator: Decreases the value of the variable by 1

public class Main {
    public static void main(String[] args) {
        int x = 5;     // Initialize variable x with value 5
        --x;            // Decrement operator: x is decreased by 1 (x becomes 4)
        System.out.println(x);  // Prints the decremented value (4)
    }
}
