// Demonstrating how to display variables with string concatenation.

public class Main {
    public static void main(String[] args) {
        
        // String variable: stores a name
        String name = "Ahmed";
        // Concatenating string with variable to display a greeting
        System.out.println("Hi " + name);
        
        // Integer variable: stores a number
        int number = 11;
        // Concatenating string with name and number to display both
        System.out.println(name + " " + number);
    }
}
