// Demonstrating Boolean types in Java.

public class Main {
    public static void main(String[] args) {
        // Boolean variables: stores true or false values
        boolean isJavaFun = true;       // True value indicating that Java is fun
        boolean isFuskaTasty = false;   // False value indicating that Fuska is not tasty (just an example)

        // Print the values of the Boolean variables
        System.out.println(isJavaFun);   // Output: true
        System.out.println(isFuskaTasty); // Output: false
    }
}
