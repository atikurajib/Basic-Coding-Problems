// Print a right-angle triangle pattern with stars (*).

public class Main {
    public static void main(String[] args) {
        // Using newlines (\n) to print the pattern
        System.out.println("*");     // First line with 1 star
        System.out.println("**");    // Second line with 2 stars
        System.out.println("***");   // Third line with 3 stars
        System.out.println("****");  // Fourth line with 4 stars
        System.out.println("*****"); // Fifth line with 5 stars
    }
}
