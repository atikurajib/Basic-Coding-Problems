// Convert string to uppercase and lowercase in Java.

public class Main {
    public static void main(String[] args) {
        String txt = "Atikur Rahman Rajib";  // Define a string

        // Convert the string to uppercase and print the result
        System.out.println(txt.toUpperCase());  // Output: "ATIKUR RAHMAN RAJIB"

        // Convert the string to lowercase and print the result
        System.out.println(txt.toLowerCase());  // Output: "atikur rahman rajib"
    }
}
