// Increment Operator: Increases the value of the variable by 1

public class Main {
    public static void main(String[] args) {
        int x = 5;     // Initialize variable x with value 5
        ++x;            // Increment operator: x is increased by 1 (x becomes 6)
        System.out.println(x);  // Prints the incremented value (6)
    }
}
