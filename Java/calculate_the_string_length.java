// Calculate the length of a string in Java.

public class Main {
    public static void main(String[] args) {
        String txt = "ABCDEFGHIJKLMNOPQRSTUVWXYZ"; // Define a string
        // Use the length() method to find the number of characters in the string
        System.out.println("The length of the txt string is: " + txt.length());  // Prints the length (26)
    }
}
